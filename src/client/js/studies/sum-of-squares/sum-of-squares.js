// Using any language of your choice, write a function that takes an integer argument, and returns true if the integer is the sum of the squares of two
// integers, and false otherwise.
// Example: f(34) = true because 34 = 3*3 + 5*5; f(35) = false
// f(5) = true because 1*1 + 2*2 = 5; f(6) = false
//
var num = 34;
function squares(number) {
    if(number = 3*3 + 5*5) {
        return true;
    }
    return false;
}

squares(num);